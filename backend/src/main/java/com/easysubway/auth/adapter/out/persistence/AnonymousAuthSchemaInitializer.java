package com.easysubway.auth.adapter.out.persistence;

import jakarta.annotation.PostConstruct;
import org.springframework.jdbc.core.JdbcTemplate;
import org.springframework.stereotype.Component;

@Component
class AnonymousAuthSchemaInitializer {

	private final JdbcTemplate jdbcTemplate;

	AnonymousAuthSchemaInitializer(JdbcTemplate jdbcTemplate) {
		this.jdbcTemplate = jdbcTemplate;
	}

	@PostConstruct
	void initialize() {
		jdbcTemplate.execute("""
			CREATE TABLE IF NOT EXISTS guest_accounts (
				user_id VARCHAR(120) NOT NULL PRIMARY KEY,
				created_at TIMESTAMP NOT NULL,
				revoked_at TIMESTAMP
			)
			""");
		jdbcTemplate.execute("""
			CREATE TABLE IF NOT EXISTS anonymous_auth_tokens (
				token_hash VARCHAR(64) NOT NULL PRIMARY KEY,
				user_id VARCHAR(120) NOT NULL,
				token_type VARCHAR(20) NOT NULL,
				issued_at TIMESTAMP NOT NULL,
				revoked_at TIMESTAMP,
				CONSTRAINT fk_anonymous_auth_tokens_user
					FOREIGN KEY (user_id) REFERENCES guest_accounts(user_id)
					ON DELETE CASCADE,
				CONSTRAINT chk_anonymous_auth_tokens_type
					CHECK (token_type IN ('ACCESS', 'REFRESH'))
			)
			""");
		jdbcTemplate.execute("""
			CREATE TABLE IF NOT EXISTS anonymous_auth_audit_events (
				event_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
				event_type VARCHAR(80) NOT NULL,
				user_id VARCHAR(120),
				occurred_at TIMESTAMP NOT NULL
			)
			""");
	}
}
