package com.easysubway.auth.adapter.out.persistence;

import jakarta.annotation.PostConstruct;
import java.util.Locale;
import org.springframework.jdbc.core.ConnectionCallback;
import org.springframework.jdbc.core.JdbcTemplate;
import org.springframework.stereotype.Component;

@Component
class AnonymousAuthSchemaInitializer {

	private final JdbcTemplate jdbcTemplate;

	AnonymousAuthSchemaInitializer(JdbcTemplate jdbcTemplate) {
		this.jdbcTemplate = jdbcTemplate;
	}

	@PostConstruct
	void initialize() {
		boolean postgresql = isPostgresql();
		jdbcTemplate.execute("""
			CREATE TABLE IF NOT EXISTS guest_accounts (
				user_id VARCHAR(120) NOT NULL PRIMARY KEY,
				created_at TIMESTAMP NOT NULL,
				revoked_at TIMESTAMP,
				CONSTRAINT chk_guest_accounts_user_id
					CHECK (char_length(trim(user_id)) > 0)
			)
			""");
		createGuestAccountsCreatedIndex(postgresql);
		jdbcTemplate.execute("""
			CREATE TABLE IF NOT EXISTS anonymous_auth_tokens (
				token_hash VARCHAR(64) NOT NULL PRIMARY KEY,
				user_id VARCHAR(120) NOT NULL,
				token_type VARCHAR(20) NOT NULL,
				issued_at TIMESTAMP NOT NULL,
				revoked_at TIMESTAMP,
				CONSTRAINT fk_anonymous_auth_tokens_user
					FOREIGN KEY (user_id) REFERENCES guest_accounts(user_id)
					ON DELETE CASCADE ON UPDATE CASCADE,
				CONSTRAINT chk_anonymous_auth_tokens_type
					CHECK (token_type IN ('ACCESS', 'REFRESH')),
				CONSTRAINT chk_anonymous_auth_tokens_hash
					CHECK (token_hash ~ '^[0-9a-f]{64}$')
			)
			""");
		jdbcTemplate.execute("""
			CREATE INDEX IF NOT EXISTS idx_anonymous_auth_tokens_user_type
			ON anonymous_auth_tokens (user_id, token_type, issued_at DESC)
			""");
		jdbcTemplate.execute("""
			CREATE TABLE IF NOT EXISTS anonymous_auth_audit_events (
				event_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
				event_type VARCHAR(80) NOT NULL,
				user_id VARCHAR(120),
				occurred_at TIMESTAMP NOT NULL
			)
			""");
		jdbcTemplate.execute("""
			CREATE INDEX IF NOT EXISTS idx_anonymous_auth_audit_events_occurred
			ON anonymous_auth_audit_events (occurred_at DESC, event_id DESC)
			""");
	}

	private boolean isPostgresql() {
		return Boolean.TRUE.equals(jdbcTemplate.execute((ConnectionCallback<Boolean>) connection ->
			connection.getMetaData().getDatabaseProductName().toLowerCase(Locale.ROOT).contains("postgresql")
		));
	}

	private void createGuestAccountsCreatedIndex(boolean postgresql) {
		if (postgresql) {
			jdbcTemplate.execute("""
				CREATE INDEX IF NOT EXISTS idx_guest_accounts_created
				ON guest_accounts (created_at DESC, user_id ASC)
				WHERE revoked_at IS NULL
				""");
			return;
		}
		jdbcTemplate.execute("""
			CREATE INDEX IF NOT EXISTS idx_guest_accounts_created
			ON guest_accounts (created_at DESC, user_id ASC)
			""");
	}
}
